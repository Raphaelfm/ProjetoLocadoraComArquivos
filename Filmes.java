package exercicio01;

public class Filmes {
	
	private int codigo;
	private String titulo;
	private char genero;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	public char getGeneroFilme() {
		return genero;
	}
	public void setGeneroFilme(char genero) {
		this.genero = genero;
	}

}
